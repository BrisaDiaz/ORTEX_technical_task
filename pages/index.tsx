import React, { useState } from "react";
import type { NextPage } from "next";
import Head from "next/head";
import ResetPasswordModal from "@/components/ResetPasswordModal";
import styles from "../styles/Home.module.css";
import FloatingCurrencyRate from "@/components/FloatingCurrencyRate";
import LoginForm from "@/components/LoginForm";
import LoadingModal from "@/components/LoadingModal";
import PopNotification from "@/components/Dialog";
import { formatCurrencyRate } from "@/utils/formatCurrencyRate";
const Home: NextPage = () => {
  const [pageState, setPageState] = React.useState<{
    isLoading: boolean;
    notification: { title: string; message: string };
  }>({
    isLoading: false,
    notification: { title: "", message: "" },
  });

  const [EURUSDMarketPrice, setEURUSDMarketPrice] = React.useState<any>(null);
  const [modalState, setModalState] = React.useState<{ isOpen: boolean }>({
    isOpen: false,
  });

  const handleOpenModal = () => {
    setModalState({ isOpen: true });
  };
  const handleCloseModal = () => {
    setModalState({ isOpen: false });
  };
  const autoClearNotifiaction = (timeout?: number) => {
    return setTimeout(() => {
      setPageState({
        notification: {
          title: "",
          message: ``,
        },
        isLoading: false,
      });
    }, timeout || 0);
  };

  const handleResetPassword = (formData: { [key: string]: string }) => {
    handleCloseModal();
    setPageState({
      ...pageState,
      isLoading: true,
    });
    /// simulate an asyncronus task
    setTimeout(() => {
      setPageState({
        notification: {
          title: "An email has been sent to you",
          message:
            "Please check your inbox for the instruccions to keep in order to reset your password. If there is't set your email address again to send you a new email.",
        },
        isLoading: false,
      });
      return autoClearNotifiaction(7000);
    }, 2000);
  };

  const handleLogin = async (formData: { [key: string]: string }) => {
    if (!formData) return;
    setPageState({
      ...pageState,
      isLoading: true,
    });
    const responce = await fetch("/api/login", {
      method: "POST",
      headers: {
        "Content-Type": "application/json",
      },
      body: JSON.stringify(formData),
    });

    const data = await responce.json();

    data.user
      ? setPageState({
          notification: {
            title: "Wellcome back!!",
            message: `User credentials: ${data.user.email}`,
          },
          isLoading: false,
        })
      : setPageState({
          notification: {
            title: "Login couldn't be completed",
            message: data.message,
          },
          isLoading: false,
        });
    return autoClearNotifiaction();
  };

  React.useEffect(() => {
    const socketConnection = new WebSocket(
      "ws://stream.tradingeconomics.com/?client=guest:guest"
    );

    socketConnection.onopen = (event: any) => {
      if (event.currentTarget.readyState == 1) {
        socketConnection.send(
          JSON.stringify({ topic: "subscribe", to: "EURUSD:CUR" })
        );
      }
    };
    socketConnection.onmessage = (message) => {
      const data = JSON.parse(message.data);

      if (data.topic === "EURUSD") {
        setEURUSDMarketPrice(formatCurrencyRate(data));
      }
    };
    socketConnection.onclose = () => {
      console.error("Websocket connection closed.");
    };
    socketConnection.onerror = (reason) => {
      console.error("Websocket error: " + reason.toString());
    };
  }, []);

  return (
    <div className={styles.container}>
      <Head>
        <title>Login | ORTEX.com</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>

      <main className={styles.main}>
        <section className={styles.currencySection}>
          <FloatingCurrencyRate data={EURUSDMarketPrice} />
        </section>
        <section className={styles.formSection}>
          <LoginForm
            onForgotPassword={handleOpenModal}
            onSubmit={handleLogin}
          />
        </section>
        <ResetPasswordModal
          isOpen={modalState.isOpen}
          onClose={handleCloseModal}
          onSubmit={handleResetPassword}
        />
        <LoadingModal isLoading={pageState.isLoading} />
        <PopNotification
          isOpen={Boolean(pageState.notification.title)}
          title={pageState.notification.title}
          message={pageState.notification.message}
          onClose={autoClearNotifiaction}
        />
      </main>
    </div>
  );
};

export default Home;
